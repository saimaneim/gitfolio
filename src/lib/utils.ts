export function getEventIcon(eventType: string) {
  switch (eventType) {
    case "PushEvent":
      return GITHUB_ICONS.push;
    case "IssuesEvent":
      return GITHUB_ICONS.issue;
    case "CreateEvent":
      return GITHUB_ICONS.create;
    case "DeleteEvent":
      return GITHUB_ICONS.delete;
    case "IssueCommentEvent":
      return GITHUB_ICONS.comment;
    case "PullRequestEvent":
      return GITHUB_ICONS.pull_request;
    case "StarEvent":
      return GITHUB_ICONS.star;
    case "ForkEvent":
      return GITHUB_ICONS.fork;
    default:
      return GITHUB_ICONS.comment;
  }
}
